class Solution:
    def reverseWords(self, s: str) -> str:

        q = []
        tmp = ""
        for i in range(0, len(s)):
            # print(s[i])
            if tmp and s[i] == " " :
                q.append(tmp)
                tmp = ""
            elif (not tmp and s[i] == " "):
                continue
            elif(i == len(s) - 1):
                if s[i] != " ":
                    tmp+=s[i]
                if tmp:
                    q.append(tmp)
            else:
                tmp += (s[i])

        res = q[::-1]
        res_string = " ".join(res)
        return res_string


