class Solution:
    def closedIsland(self, grid: List[List[int]]) -> int:

        m = len(grid)
        n = len(grid[0])

        def dfs(grid, i, j):
            if i < 0 or i >= m or j < 0 or j >= n:
                return

            if grid[i][j] != 0:
                return
            grid[i][j] = -1
            dfs(grid, i - 1, j)
            dfs(grid, i, j - 1)
            dfs(grid, i + 1, j)
            dfs(grid, i, j + 1)

        for i in [0,m-1]:
            for j in range(0,n):
                dfs(grid,i,j)
        for i in range(0,m):
            for j in [0,n-1]:
                dfs(grid,i,j)

        res = 0
        for i in range(0,m):
            for j in range(0,n):
                if grid[i][j] == 0:
                    dfs(grid,i,j)
                    res+=1

        return  res


